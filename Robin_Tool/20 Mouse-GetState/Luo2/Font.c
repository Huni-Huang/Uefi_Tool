//Function Name: putASCII
//Input: x,y,code=ascii code,mag=magnification
//Description: display ascii code
VOID  putASCII(UINTN x,UINTN y,UINT8 code,EFI_GRAPHICS_OUTPUT_BLT_PIXEL *color,UINT16 mag)
{
  UINT16  i,j,index,m,n;
  
  for(i=0;i<ASCIIMAX;i++)
    if(ascii_table[i].code == code)
      break;
  index = i;
  for(j=0;j<CHARHEIGHT;j++)
    for(i=0;i<CHARWIDTH;i++)
    {
      if(ascii_table[index].array[j] & (1<<(CHARWIDTH-i)))
        for(n=0;n<mag;n++)
          for(m=0;m<mag;m++)
            putpixel(x+(UINTN)(mag*i+m),y+(UINTN)(mag*j+n),color);  
    }
} 
//Function Name: putHZ16
//Input: x,y,code=Chinese code,mag=magnification
//Description: display Chinese code
VOID  putHZ16(UINTN x,UINTN y,UINT16 code,EFI_GRAPHICS_OUTPUT_BLT_PIXEL *color,UINT16 mag)
{
  UINT16  i,j,index,m,n;
  UINT16 zimo;
  UINT16 buff;
    
  for(i=0;i<HZCOUNT;i++)              /* The number of Chinese words has been given */
    if(hzk16_table[i].code == code)
      break;
      
  index = i;
  if(index ==HZCOUNT) return;
  for(j=0;j<HZHEIGHT;j++)
  {
    zimo=hzk16_table[index].array[j];
//    asm mov ax,zimo;
//    asm xchg  al,ah
//    asm mov zimo,ax
    //exchange zimo low byte and high byte
    buff=zimo;
    buff=((buff>>8) &0x00ff);
    zimo=((zimo<<8) & 0xff00);
    zimo=(zimo|buff);
    
    for(i=0;i<HZWIDTH;i++)
      if((zimo>>(HZWIDTH-i))&0x01)
        for(n=0;n<mag;n++)
          for(m=0;m<mag;m++)
            putpixel(x+(UINTN)(mag*i+m),y+(UINTN)(mag*j+n),color);  
  }
}
//Function Name: putStr
//Input: x,y,s=pointer to string,mag=magnification
//Description: display Chinese code
VOID  putStr(UINTN x,UINTN y,UINT8 *s,EFI_GRAPHICS_OUTPUT_BLT_PIXEL *color,UINT16 mag)
{
  UINT16 hzwidth,hzheight;
  UINT16 charwidth,charheight;
  UINT16 i=0,hz=0;
  
  hzwidth=hzheight=16;  //chinese word widthd and height 16x16      
  charwidth=8;          //ASCII code 8x16
  charheight=16;
  
  while(*(s+i))
  {
    if(s[i]<128)
    {
      putASCII(x,(y+(hzheight-charheight)*mag),s[i],color,mag);
      i++;
      x+=(charwidth*mag);   
    }
    else
    {
      hz=(unsigned int)(s[i+1]);
      hz<<=8;
      hz+=(unsigned int)(s[i]);
      putHZ16(x,y,hz,color,mag);
      i+=2;
      x+=(hzwidth*mag);
    }
  }
}