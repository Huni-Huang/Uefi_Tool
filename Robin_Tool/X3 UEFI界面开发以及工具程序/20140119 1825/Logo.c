//========================= Logo function =========================================
//Function Name: ShowLogo
//Input: x,y,Logo_Width,Logo_Height,LogoColorTable=Pointer to logo color table,
//       LogoPicture=picture of logo,MaskColor=not display color in LogoPicture
//Output: None

VOID ShowLogo(UINTN x,UINTN y,UINTN Logo_Width,UINTN Logo_Height,
              EFI_GRAPHICS_OUTPUT_BLT_PIXEL *LogoColorTable,
              UINT8 *LogoPicture,
              UINT8 MaskColor)
{
  UINTN i,j;
  UINT16 index=0;
  UINT8 color_number;
  for(j=y;j<Logo_Height+y;j++)
    for(i=x;i<Logo_Width+x;i++)
    {
      color_number=LogoPicture[index];
      if(color_number!=MaskColor)
        putpixel(i,j,&(LogoColorTable[color_number]));
      ++index;
    }
}